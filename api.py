import asyncio

@router.post('/api/chat/completions')
async def chat_completions(req: dict = Body(...), request: Request = None):
    """Proxy chat requests to the local LLM based on config.yaml"""
    config = _load_config()

    # Allow frontend request to override backend global config
    base_url = req.get("base_url") or config.base_url
    api_key = req.get("api_key") or config.api_key
    model_name = req.get("model_name") or config.model_name
    system_prompt = req.get("system_prompt")
    if system_prompt is None:
        system_prompt = config.system_prompt
    extra_params = req.get("extra_params")
    if extra_params is None:
        extra_params = config.extra_params

    base_url = base_url.rstrip('/')
    if not base_url:
        raise HTTPException(status_code=400, detail="Base URL is not configured")

    url = f"{base_url}/chat/completions"
    headers = {
        "Authorization": f"Bearer {api_key}",
        "Content-Type": "application/json"
    }

    messages = req.get("messages", [])
    if system_prompt and isinstance(system_prompt, str) and system_prompt.strip():
        # Inject system prompt if it's not already the first message
        if not messages or messages[0].get("role") != "system":
            messages.insert(0, {"role": "system", "content": system_prompt})

    payload = {
        "model": model_name,
        "messages": messages,
        "stream": req.get("stream", False),
    }

    if extra_params and isinstance(extra_params, dict):
        for k, v in extra_params.items():
            if k not in payload:
                payload[k] = v

    for k in ["temperature", "top_p", "max_tokens", "presence_penalty", "frequency_penalty"]:
        if k in req:
            payload[k] = req[k]

    stream = payload["stream"]

    try:
        if not stream:
            async with httpx.AsyncClient() as client:
                start_time = time.time()
                resp = await client.post(url, headers=headers, json=payload, timeout=300.0)
                resp.raise_for_status()
                data = resp.json()
                end_time = time.time()

                if "usage" in data:
                    data["usage"]["total_time"] = end_time - start_time

                return data
        else:
            async def generate():
                async with httpx.AsyncClient() as client:
                    start_time = time.time()
                    first_token_time = None
                    total_chars = 0
                    try:
                        # timeout config
                        async with client.stream("POST", url, headers=headers, json=payload, timeout=300.0) as resp:
                            if resp.status_code != 200:
                                error_text = await resp.aread()
                                error_msg = error_text.decode('utf-8', errors='ignore')
                                logger.error(f"Upstream API error ({resp.status_code}): {error_msg}")
                                yield f"data: {json.dumps({'error': f'Upstream API error {resp.status_code}: {error_msg}'})}\n\n"
                                return

                            async for chunk in resp.aiter_lines():
                                if chunk.startswith("data: "):
                                    chunk_data = chunk[6:]
                                    if chunk_data == "[DONE]":
                                        yield chunk + "\n\n"
                                        break

                                    try:
                                        parsed = json.loads(chunk_data)
                                        choices = parsed.get("choices", [])
                                        if choices and "delta" in choices[0]:
                                            delta = choices[0]["delta"]
                                            content = delta.get("content", "")
                                            if content:
                                                if first_token_time is None:
                                                    first_token_time = time.time()
                                                total_chars += len(content)

                                        yield chunk + "\n\n"

                                        # Minimum refresh interval logic.
                                        # Yielding slightly throttled to allow frontend UI to stay responsive
                                        await asyncio.sleep(0.01)
                                    except json.JSONDecodeError:
                                        yield chunk + "\n\n"
                                else:
                                    if chunk.strip():
                                        yield chunk + "\n\n"

                            end_time = time.time()
                            ttft = (first_token_time - start_time) if first_token_time else 0
                            total_time = end_time - start_time
                            telemetry = {
                                "ttft": ttft,
                                "total_time": total_time,
                                "total_chars": total_chars
                            }
                            yield f"data: [TELEMETRY] {json.dumps(telemetry)}\n\n"
                    except Exception as e:
                        logger.error(f"Streaming error: {e}")
                        yield f"data: {json.dumps({'error': str(e)})}\n\n"

            return StreamingResponse(generate(), media_type="text/event-stream")

    except Exception as e:
        logger.error(f"Chat completion error: {e}")
        raise HTTPException(status_code=500, detail=str(e))



