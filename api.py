SESSIONS_DIR = 'sessions'
if not os.path.exists(SESSIONS_DIR):
    os.makedirs(SESSIONS_DIR)

@router.get('/api/sessions', response_model=models.ResponseModel)
async def list_sessions():
    """List all saved chat sessions"""
    sessions = []
    try:
        if os.path.exists(SESSIONS_DIR):
            files = [f for f in os.listdir(SESSIONS_DIR) if f.endswith('.json')]
            # sort by modification time descending
            files.sort(key=lambda x: os.path.getmtime(os.path.join(SESSIONS_DIR, x)), reverse=True)
            for file in files:
                try:
                    with open(os.path.join(SESSIONS_DIR, file), 'r', encoding='utf-8') as f:
                        data = json.load(f)
                        messages = data.get('messages', [])
                        create_time = None
                        for msg in messages:
                            if msg.get('role') == 'user' and msg.get('time'):
                                create_time = msg.get('time')
                                break

                        sessions.append({
                            'id': file.replace('.json', ''),
                            'title': data.get('title', 'New Chat'),
                            'config_name': data.get('config_name'),
                            'create_time': create_time,
                            'update_time': os.path.getmtime(os.path.join(SESSIONS_DIR, file))
                        })
                except Exception as e:
                    logger.warning(f'Failed to parse session file {file}: {e}')
                    pass
        return {'code': 0, 'message': 'success', 'data': sessions}
    except Exception as e:
        logger.error(f'Failed to list sessions: {e}')
        return {'code': 1, 'message': str(e), 'data': []}

